#include "../includes/ft_printf.h"

int		flag_0(const char *f, int t)
{
	int b;

	b = 0;
	while ((f[t] < '0' || f[t] > '9') && f[t] != '\0' && f[t] != '.')
		t++;
	if (f[t] == '0')
		b = 1;
	return (b);
}
